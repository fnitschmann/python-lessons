# list manipulations - exercise 18
cities = ["St. Petersburg", "Moscow", "Buenos Aires", "New York", "Stockholm",
        "Amsterdam"]

print(cities)
print(sorted(cities))
print(cities)
print(sorted(cities, reverse = True))
print(cities)

cities.reverse()
print(cities)

cities.reverse()
print(cities)

cities.sort()
print(cities)

cities.sort(reverse = True)
print(cities)
